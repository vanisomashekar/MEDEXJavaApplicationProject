    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        int selectedRow = bidcond.getSelectedRow();
        if(selectedRow<0)
        {
             JOptionPane.showMessageDialog(null, "Please select a row from the table to send it to ThirdParty", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if(msg.getText().equals(""))
        {
             JOptionPane.showMessageDialog(null, "Please enter a message to the ThirdParty");
        }
        else
        {
         

        if (selectedRow < 0){
            return;
        }
         Order o=(Order)bidcond.getValueAt(selectedRow, 2);
      //  String message = messageJTextField.getText();
        
         
      TpWorkRequest request = new TpWorkRequest();
      
     
       //
      Product p=(Product)bidcond.getValueAt(selectedRow, 0);
     //  p.setProStatus("Bided");
       request.setMessage(msg.getText());
      request.setProduct(p.getName());
        request.setSender(account);
        request.setStatus("SentFromSupplier");
       request.setOrder(o);
        
     Organization org = null;
        for(Network network:business.getNetworkList()){
            for(Enterprise enterprise:network.getEnterpriseDirectory().getEnterpriseList()){
                for(Organization organization1:enterprise.getOrganizationDirectory().getOrganizationList()){
            if (organization1 instanceof TpManagementOrganization){
                org = organization1;
                break;
            }
        }
            }
        }
        if (org!=null){
            org.getWorkQueue().getWorkRequestList().add(request);
            account.getWorkQueue().getWorkRequestList().add(request);
            JOptionPane.showMessageDialog(null, "Order has been sent to ThirdParty");
            p.setStatus("Completed");
        }
        
    }//GEN-LAST:event_jButton2ActionPerformed
    }
