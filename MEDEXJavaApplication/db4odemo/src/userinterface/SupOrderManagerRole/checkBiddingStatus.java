     public void populateRequestTable(){
        DefaultTableModel model = (DefaultTableModel) bidcond.getModel();
        
        DefaultTableCellRenderer headerRenderer = new DefaultTableCellRenderer();
        headerRenderer.setBackground(new Color(85,79,102));

        for (int i = 0; i < bidcond.getModel().getColumnCount(); i++) {
       bidcond.getColumnModel().getColumn(i).setHeaderRenderer(headerRenderer);
       }
        
        model.setRowCount(0);
         Organization org = null;
        for(Network network:business.getNetworkList()){
            for(Enterprise enterprise:network.getEnterpriseDirectory().getEnterpriseList()){
                for(Organization organization1:enterprise.getOrganizationDirectory().getOrganizationList()){
            if (organization1 instanceof CustomerProcurementOrganization){
                org = organization1;
                break;
            }
        }
            }
        }
        if (org!=null){
       // org.getWorkQueue().getWorkRequestList().clear();
        for (WorkRequest request : org.getWorkQueue().getWorkRequestList()){
            if(request.getStatus().equals("Approve"))
            {
                
            
            Object[] row = new Object[4];
            Product p=((BidWorkRequest) request).getPro();
            row[0] = p;
            row[1] = request.getStatus();
            Order o=((BidWorkRequest) request).getOrder();
            row[2]=o;
            model.addRow(row);
        }
            else 
            {
                DefaultTableModel model1 = (DefaultTableModel)rejectedTable.getModel();
        
                model1.setRowCount(0);
                 Object[] row = new Object[4];
            Product p=((BidWorkRequest) request).getPro();
            
            row[0] = p;
            row[1] = request.getStatus();
            Order o=((BidWorkRequest) request).getOrder();
            row[2]=o;
            model1.addRow(row);
          
                
            }
        }
   
     }   
        
     }
     
    

